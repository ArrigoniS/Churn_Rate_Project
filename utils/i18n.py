# ─────────────────────────────────────────────

def get_lang() -> str:
    """Restituisce la lingua attiva ('it' o 'en')."""
    return st.session_state.get("lang", "en")


def t(key: str) -> str:
    """Restituisce il testo nella lingua attiva."""
    lang = get_lang()
    return TEXTS.get(key, {}).get(lang, key)


def render_lang_toggle():
    """
    Mostra il toggle lingua nella sidebar.
    Va chiamato una volta sola in ogni pagina, dopo st.set_page_config.
    """
    with st.sidebar:
        st.markdown("---")
        lang = st.session_state.get("lang", "it")
        choice = st.radio(
            "🌐 Language / Lingua",
            options=["🇮🇹 Italiano", "🇬🇧 English"],
            index=0 if lang == "it" else 1,
            key="lang_radio",
            horizontal=True,
            label_visibility="collapsed",
        )
        new_lang = "it" if "Italiano" in choice else "en"
        if new_lang != lang:
            st.session_state["lang"] = new_lang
            st.rerun()
        st.markdown("---")


